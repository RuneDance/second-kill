package com.yyt.secondkill.entity;

/**
 * 用户信息
 */
public class User {

    //用户ID
    private int id;

    //用户名
    private String name;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
